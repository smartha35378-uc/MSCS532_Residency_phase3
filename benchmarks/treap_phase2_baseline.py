"""
inventory_treap.py

A refactored Treap (tree + heap) implementation tailored for *dynamic inventory management*.

Why a Treap for inventory?
- Keys (e.g., SKU strings) are stored in BST order -> fast search and sorted traversal.
- Random priorities maintain expected balance -> expected O(log n) insert/search/delete.
- We can also support range queries (e.g., SKU ranges) efficiently using BST structure.

Design notes
- This implementation uses a **max-heap** on priority:
    Parent priority >= child priority
  (This is a common convention; a min-heap variant is equally valid if rotations are inverted.)
- Priorities are generated by a random number generator that can be seeded for repeatable tests/demos.
"""

from __future__ import annotations

from dataclasses import dataclass
from typing import Any, Callable, Generator, Iterable, Optional, Tuple, List
import random


# -----------------------------
# Domain model for inventory
# -----------------------------

@dataclass
class InventoryItem:
    """
    A simple inventory record.

    You can extend this later with:
    - location/bin
    - supplier info
    - reorder thresholds
    - last_updated timestamps, etc.
    """
    sku: str
    name: str
    quantity: int
    price: float


# -----------------------------
# Treap core data structures
# -----------------------------

class TreapNode:
    """
    A Treap node stores:
    - key: used for BST ordering (SKU in this project)
    - value: associated payload (InventoryItem)
    - priority: random heap key used to probabilistically balance the tree
    - left/right: child references
    """

    __slots__ = ("key", "value", "priority", "left", "right")

    def __init__(self, key: str, value: InventoryItem, priority: int):
        self.key: str = key
        self.value: InventoryItem = value
        self.priority: int = priority
        self.left: Optional["TreapNode"] = None
        self.right: Optional["TreapNode"] = None


class InventoryTreap:
    """
    Treap wrapper class offering inventory-centric operations.

    Core operations (PoC):
    - insert(item): insert new SKU or replace existing SKU's record
    - search(sku): find an item by SKU
    - delete(sku): remove an item by SKU
    - inorder(): iterate items in sorted SKU order
    - range_query(low, high): retrieve items with SKU within [low, high]

    Inventory-friendly operations (recommended for dynamic inventory):
    - update_quantity(sku, delta): adjust stock levels (+/-)
    - set_quantity(sku, qty): overwrite stock level
    """

    def __init__(self, seed: Optional[int] = None):
        # Root pointer of the Treap.
        self._root: Optional[TreapNode] = None

        # A dedicated RNG lets us seed priorities for repeatable demos/tests.
        self._rng = random.Random(seed)

    # -----------------------------
    # Rotations (Treap balancing)
    # -----------------------------

    @staticmethod
    def _rotate_right(y: TreapNode) -> TreapNode:
        """
        Right rotation around y:
            y               x
           / \             / \
          x   T3   ==>    T1  y
         / \                 / \
        T1  T2              T2  T3
        """
        x = y.left
        if x is None:
            return y  # Shouldn't happen if used correctly, but safe-guard.
        T2 = x.right

        # Perform rotation.
        x.right = y
        y.left = T2
        return x

    @staticmethod
    def _rotate_left(x: TreapNode) -> TreapNode:
        """
        Left rotation around x:
          x                  y
         / \                / \
        T1  y      ==>     x  T3
           / \            / \
          T2  T3         T1  T2
        """
        y = x.right
        if y is None:
            return x
        T2 = y.left

        # Perform rotation.
        y.left = x
        x.right = T2
        return y

    # -----------------------------
    # Public API
    # -----------------------------

    def insert(self, item: InventoryItem) -> None:
        """
        Insert an inventory item into the treap.

        Behavior on duplicate SKU:
        - Replace the existing record (common for "upsert" behavior).
        """
        priority = self._rng.randint(1, 1_000_000_000)
        self._root = self._insert(self._root, item.sku, item, priority)

    def search(self, sku: str) -> Optional[InventoryItem]:
        """Return the InventoryItem for sku, or None if not found."""
        node = self._search(self._root, sku)
        return node.value if node else None

    def delete(self, sku: str) -> bool:
        """
        Delete the item with the given SKU.

        Returns:
            True if an item was deleted, False if sku wasn't found.
        """
        self._root, deleted = self._delete(self._root, sku)
        return deleted

    def inorder(self) -> List[InventoryItem]:
        """Return all items in sorted (inorder) SKU order."""
        return [node.value for node in self._inorder_nodes(self._root)]

    def range_query(self, low_sku: str, high_sku: str) -> List[InventoryItem]:
        """
        Return items whose SKU is in the inclusive range [low_sku, high_sku].

        This is useful for:
        - pagination by SKU
        - reporting on subsets of inventory
        - grouping by SKU prefixes if combined with careful bounds
        """
        results: List[InventoryItem] = []
        self._range_query(self._root, low_sku, high_sku, results)
        return results

    def update_quantity(self, sku: str, delta: int) -> bool:
        """
        Adjust quantity for an existing SKU by delta.

        Example use cases:
        - Receiving shipments (+)
        - Customer sales / order fulfillment (-)
        - Inventory adjustments (+/-)

        Returns:
            True if updated, False if sku not found.
        """
        node = self._search(self._root, sku)
        if node is None:
            return False
        node.value.quantity += delta
        return True

    def set_quantity(self, sku: str, quantity: int) -> bool:
        """Set quantity for an existing SKU. Returns False if sku not found."""
        if quantity < 0:
            raise ValueError("quantity cannot be negative")
        node = self._search(self._root, sku)
        if node is None:
            return False
        node.value.quantity = quantity
        return True

    def __len__(self) -> int:
        """Count nodes (O(n)); adequate for PoC, can be optimized later."""
        return sum(1 for _ in self._inorder_nodes(self._root))

    # -----------------------------
    # Internal helpers
    # -----------------------------

    def _insert(self, root: Optional[TreapNode], key: str, value: InventoryItem, priority: int) -> TreapNode:
        """
        Standard BST insert, then rotate to maintain heap property (max-heap).

        Heap property (max-heap):
            root.priority >= child.priority
        """
        # Base case: empty subtree => create a new node.
        if root is None:
            return TreapNode(key, value, priority)

        # Duplicate key => replace payload (upsert). Keep existing priority to avoid churn.
        if key == root.key:
            root.value = value
            return root

        # BST step: recurse left or right.
        if key < root.key:
            root.left = self._insert(root.left, key, value, priority)

            # If left child violates heap property, rotate right.
            if root.left and root.left.priority > root.priority:
                root = self._rotate_right(root)
        else:
            root.right = self._insert(root.right, key, value, priority)

            # If right child violates heap property, rotate left.
            if root.right and root.right.priority > root.priority:
                root = self._rotate_left(root)

        return root

    def _search(self, root: Optional[TreapNode], key: str) -> Optional[TreapNode]:
        """Standard BST search."""
        cur = root
        while cur is not None:
            if key == cur.key:
                return cur
            cur = cur.left if key < cur.key else cur.right
        return None

    def _delete(self, root: Optional[TreapNode], key: str) -> Tuple[Optional[TreapNode], bool]:
        """
        Delete a node by key while preserving treap invariants.

        Strategy:
        1) BST search for key.
        2) When found, rotate the node down until it becomes a leaf,
           choosing the rotation direction based on child priorities.
        3) Remove leaf.

        Returns:
            (new_root, deleted_flag)
        """
        if root is None:
            return None, False

        if key < root.key:
            root.left, deleted = self._delete(root.left, key)
            return root, deleted

        if key > root.key:
            root.right, deleted = self._delete(root.right, key)
            return root, deleted

        # Found the node to delete.
        return self._delete_root_node(root), True

    def _delete_root_node(self, root: TreapNode) -> Optional[TreapNode]:
        """
        Delete the given root node by rotating it downward until removable.

        Cases:
        - If one child is None -> replace node with the other child.
        - If both children exist -> rotate toward the higher-priority child (max-heap),
          so the higher-priority child becomes the new root of this subtree.
        """
        # If leaf or single-child, just bypass.
        if root.left is None:
            return root.right
        if root.right is None:
            return root.left

        # Both children exist: rotate with the child that has higher priority.
        if root.left.priority > root.right.priority:
            # Right rotate, then delete from the right subtree (where root moved).
            new_root = self._rotate_right(root)
            new_root.right = self._delete_root_node(new_root.right)  # type: ignore[arg-type]
            return new_root
        else:
            # Left rotate, then delete from the left subtree (where root moved).
            new_root = self._rotate_left(root)
            new_root.left = self._delete_root_node(new_root.left)  # type: ignore[arg-type]
            return new_root

    def _inorder_nodes(self, root: Optional[TreapNode]) -> Generator[TreapNode, None, None]:
        """Yield nodes in inorder traversal (sorted by key)."""
        if root is None:
            return
        yield from self._inorder_nodes(root.left)
        yield root
        yield from self._inorder_nodes(root.right)

    def _range_query(self, root: Optional[TreapNode], low: str, high: str, out: List[InventoryItem]) -> None:
        """Inorder-like traversal with pruning based on [low, high] bounds."""
        if root is None:
            return

        # If root.key is greater than low, left subtree may contain valid keys.
        if low < root.key:
            self._range_query(root.left, low, high, out)

        # If root.key is within bounds, include it.
        if low <= root.key <= high:
            out.append(root.value)

        # If root.key is less than high, right subtree may contain valid keys.
        if root.key < high:
            self._range_query(root.right, low, high, out)

    # -----------------------------
    # Convenience constructors
    # -----------------------------

    @classmethod
    def from_items(cls, items: Iterable[InventoryItem], seed: Optional[int] = None) -> "InventoryTreap":
        """Build a treap from an iterable of items."""
        t = cls(seed=seed)
        for it in items:
            t.insert(it)
        return t
